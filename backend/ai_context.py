def _training_flag(user_id: str) -> str:
    """Today's injury/discomfort flag, if the user set one. Coach Al
    must acknowledge by name and skip movements loading the affected
    area."""
    try:
        import training_flags as _tf
        from datetime import date as _d
        return _tf.context_block_for_coach(_tf.today_flag(user_id, _d.today().isoformat()))
    except Exception:
        return ""


def build(user_id: str, profile: Optional[dict] = None) -> dict:
    """Assemble the shared AI context bundle. Every AI surface (chat,
    briefing, today_workout, daily_insight) reads from this — one
    canonical bundle means the surfaces never drift out of sync with
    what the app is actually showing the user.

    Returns a dict of preformatted strings + raw structured data.
    Missing sections come back as "" (safe to concatenate).

    IMPORTANT: keys used for prompt-block strings use the `_ctx` suffix
    so they don't collide with older code paths that set the same
    key with structured dicts (main.py's briefing endpoint used
    "active_goal" to mean the goal-progress dict, not a prompt block).
    The overwrite crashed the briefing on July 5 — dict got joined into
    a string parts list. Suffix keeps namespaces separate."""
    profile = profile or {}
    return {
        "freshness_advisory":     _freshness(user_id),
        "clinical_escalation":    _clinical(user_id, profile),
        "active_visit_ctx":       _active_visit(user_id),
        "training_flag_ctx":      _training_flag(user_id),
        "active_goal_ctx":        _active_goal(user_id),
        "recent_insights_ctx":    _recent_insights(user_id),
        "weekly_recap_ctx":       _weekly_recap(user_id),
        "data_quality_flags":     _data_quality_flags(user_id),
        "manual_readings_ctx":    _manual_readings(user_id),
    }


def merge_into(health_context: dict, user_id: str, profile: Optional[dict] = None) -> dict:
    """Convenience for existing callers: merge the AI context bundle
    into an existing health_context dict without clobbering keys the
    caller has already set. Preserves the old prompt-key names so
    chat.py / briefing.py / today_workout.py don't need to change
    their prompt assembly."""
    bundle = build(user_id, profile)
    for k, v in bundle.items():
        health_context.setdefault(k, v)
    return health_context


__all__ = ["build", "merge_into"]
